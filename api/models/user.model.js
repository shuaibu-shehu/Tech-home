const mongoose = require("mongoose");

const userSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      unique: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
    avatar: {
      type: String,
      default: "https://www.pngall.com/wp-content/uploads/5/Profile.png",
    },
  },
  { timestamps: true }
);
module.exports = mongoose.model("User", userSchema);
